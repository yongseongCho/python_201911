# 문자열의 배열 연산
# 문자열 변수는 배열 변수로서 인덱스 연산을
# 통해 각 문자에 접근할 수 있습니다.
# 인덱스의 시작은 0
msg = 'Hello Python~!'

# msg 변수에 저장된 문자열의 첫번째 문자 출력
print('msg[0] -> {}'.format(msg[0]))
# msg 변수에 저장된 문자열의 7번째 문자 출력
print('msg[6] -> {}'.format(msg[6]))
# msg 변수에 저장된 문자열의 14번째 문자 출력
print('msg[13] -> {}'.format(msg[13]))

# 배열의 -1 인덱스는 마지막 요소를 가리킵니다.
print('msg[-1] -> {}'.format(msg[-1]))

print('msg[-2] -> {}'.format(msg[-2]))











