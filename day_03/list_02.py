# 중첩된 형태의 리스트 변수 선언
# 리스트 내부에 리스트 변수의 선언이 가능
nested_list = [1,2,3,['a','b','c']]

print( nested_list[1] )

# 중첩된 리스트에 접근하는 방법
# 중첩된 인덱스를 사용하여 접근
print( nested_list[3] )
print( nested_list[3][0] )
print( nested_list[3][1] )
print( nested_list[3][2] )













