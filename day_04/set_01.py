# Set 타입의 데이터
# 데이터의 중복을 허용하지 않는 자료형
# 순서를 유지하지 않음

# Set 타입의 변수 선언
# 변수명 = set([데이터1, 데이터2, ...])

numbers = set([1,2,3,4,5])
print(type(numbers))
print(numbers)

numbers = set([1,1,2,2,3,3,4,5])
print(numbers)

message = set('Hello World')
print(message)






