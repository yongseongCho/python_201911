# 함수(Function)
# 기능을 정의하는 단위
# 코드의 재활용을 위해서 사용되는 문법
# 프로그램을 개발하는 도중 반복되서 사용되는 코드의 묶음을
# 함수로 정의하여 재활용할 수 있음

# def 함수명( [매개변수] ) :
#   함수의 호출 시, 실행할 실행문...
def hello() :
    print('hello 함수 실행!')

# 함수는 선언된 이후(만들어진 이후),
# 해당함수를 호출하는 실행 코드에 의해서 실행됩니다.

# 함수의 호출
# 함수명([해당함수에 필요한 매개변수])
hello()

# 함수는 선언된 이후에 계속해서 호출할 수 있습니다.
